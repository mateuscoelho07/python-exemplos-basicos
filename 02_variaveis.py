# variaveis
nome = "Sabrina Carpenter"
idade = 25

#exibição de valores
print(nome)
print(idade)

# Primeiro método de concatenação (string: str)
print("O meu nome é: "+ nome + "e tenho " +str(idade) + "anos.\n")

# Segundo método de concatenação (Format)
print ("O meu nome é: () e tenho () anos.\n".format(nome,idade))
         
# Terceiro método de concatenação (f string)
print(f"O meu nome é: {nome} e tenho {idade} anos.")
print("\n")